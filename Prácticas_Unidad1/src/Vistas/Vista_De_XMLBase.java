    private void txtEscribirKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEscribirKeyPressed
        // TODO add your handling code here:
        //Escribir c√≥digo para agregar datos al vector y mostrarlos a la tabla. Borrar el campo de texto.
        Datos dato = new Datos();
        try {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                for (int iteracion = 0; iteracion < evt.getKeyCode(); iteracion++) {
                    dato.setDatos(txtEscribir.getText());
                    JOptionPane.showConfirmDialog(this, dato.getDatos());
                    //Guardarlo en el vector de XMLBase
                    boolean respuesta = contenido.insertar(dato, iteracion);
                    JOptionPane.showConfirmDialog(this, dato.getDatos());
                    // JOptionPane.showConfirmDialog(this, dato.getDatos());

                    modeloDatos.addRow(new Object[]{dato.getDatos()});
                    txtEscribir.setText("");
                }

            }
        } catch (NullPointerException error) {
            //JOptionPane.showMessageDialog(this, error);
        }

    }//GEN-LAST:event_txtEscribirKeyPressed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        //Si hay datos en el modelo de la tabla. y en la tabla.
        try {
            int posicion = Integer.parseInt(txtEscribir.getText());
            Datos buscando = contenido.buscar(posicion);
            if (buscando == null) {
                JOptionPane.showMessageDialog(this, "No se pudo encontrar lo escrito.");
            } else {
                JOptionPane.showMessageDialog(this, buscando.getDatos());
                lblcaliz.setText(buscando.getDatos());
            }
        } catch (NullPointerException error) {

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

